/* Given an array of integers arr, return true if the number of occurrences of each value in the array is unique or false otherwise.

Example 1:

Input: arr = [1,2,2,1,1,3]
Output: true
Explanation: The value 1 has 3 occurrences, 2 has 2 and 3 has 1. No two values have the same number of occurrences.
Example 2:

Input: arr = [1,2]
Output: false
Example 3:

Input: arr = [-3,0,1,-3,1,1,1,-3,10,0]
Output: true */

#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>
#include <assert.h>

int uniqueOccurrences(int *arr, int arrSize) {
	int *frequencies = calloc(2001, sizeof(int));

	for (int i = 0; i < arrSize; i++) {
		frequencies[arr[i] + 1000]++;
	}

	int *occurrences = calloc(1005, sizeof(int));

	for (int i = 0; i <= 2000; i++) {
		if (frequencies[i] == 0) {
			continue;
		}
		else if (occurrences[frequencies[i]] == 0) {
			occurrences[frequencies[i]] = 1;
		}
		else {
			return false;
		}
	}

	return true;
}

